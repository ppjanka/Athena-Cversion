void problem(Grid *pGrid, Domain *pDomain)
{
  int i=0,j=0,k=0;
  int is,ie,js,je,ks,ke;
  Real n0,T0,kappa;
  long int iseed = -1;

  is = pGrid->is; ie = pGrid->ie;
  js = pGrid->js; je = pGrid->je;
  ks = pGrid->ks; ke = pGrid->ke;

/* Read problem parameters */

  T0    = par_getd("problem","T0");
  n0    = par_getd("problem","n0");
#ifdef ISOTROPIC_CONDUCTION
  kappa = par_getd("problem","kappa");
  kappa_T = (mbar/kb)*kappa;
#endif

/* Constant density and temperature initially */

  for (k=ks; k<=ke; k++) {
  for (j=js; j<=je; j++) {
    for (i=is; i<=ie; i++) {
      pGrid->U[k][j][i].d = n0*mbar;
      pGrid->U[k][j][i].M1 = 0.0;
      pGrid->U[k][j][i].M2 = 0.0;
      pGrid->U[k][j][i].M3 = 0.0;
/* Pressure scaled to give a sound speed of 1 with gamma=1.4 */
      pGrid->U[k][j][i].E = (n0*kb*T0)/Gamma_1
             + 0.5*(SQR(pGrid->U[k][j][i].M1) + SQR(pGrid->U[k][j][i].M2)
             + SQR(pGrid->U[k][j][i].M3))/pGrid->U[k][j][i].d;
    }
  }}

  CoolingFunc = KoyInut;

}

